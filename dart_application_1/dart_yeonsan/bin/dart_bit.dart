// lib/dart_bit.dart

void main(){
  int a = 5;    //10진수
  int b = 0x03; //16진수

  // 5 : 0000101
  // 3 : 0000011

  print(a & b); // 0000001 =>  1
  print(a | b); // 0000111 =>  7
  print(a ^ b); // 0000110 =>  6 
  print(a >> b);// 0000000 =>  0 왼쪽에서 오른쪽으로 밀어냄
  print(a << b);// 0101000 =>  40 오른쪽에서 왼쩍으로 밀어냄
}